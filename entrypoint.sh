#!/bin/bash
# Updated entrypoint.sh for field rename migrations - preserves data
set -e

echo "üöÄ Starting Trading Admin deployment with field rename migrations..."
echo "üåê Port configuration: ${PORT:-10000}"

# Set Django settings for production
export DJANGO_SETTINGS_MODULE=${DJANGO_SETTINGS_MODULE:-trading_admin.settings_render}

# Wait for PostgreSQL database
echo "‚è≥ Waiting for PostgreSQL database..."
python -c "
import os, sys, time
try:
    import psycopg2
    from urllib.parse import urlparse
    
    db_url = os.getenv('DATABASE_URL')
    if not db_url:
        print('‚ö†Ô∏è  No DATABASE_URL - using SQLite')
        sys.exit(0)
        
    parsed = urlparse(db_url)
    
    max_attempts = 30
    for attempt in range(max_attempts):
        try:
            conn = psycopg2.connect(
                host=parsed.hostname,
                port=parsed.port or 5432,
                user=parsed.username,
                password=parsed.password,
                dbname=parsed.path[1:],
                connect_timeout=10
            )
            conn.close()
            print('‚úÖ PostgreSQL connected')
            break
        except psycopg2.OperationalError:
            if attempt >= max_attempts - 1:
                print('‚ùå Database timeout')
                sys.exit(1)
            time.sleep(2)
            
except ImportError:
    print('‚ö†Ô∏è  psycopg2 not available')
except Exception as e:
    print(f'‚ö†Ô∏è  Database check failed: {e}')
"

# Check database state and determine migration strategy
echo "üîç Analyzing database state for field rename migration..."
python -c "
import os, django
os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'trading_admin.settings_render')
django.setup()

from django.db import connection
from django.core.management import call_command

print('üîç Checking database tables and field structure...')

tables_exist = []
field_structure = {}

with connection.cursor() as cursor:
    # Check if main tables exist
    required_tables = [
        'configurations_tradingconfiguration',
        'licenses_client', 
        'licenses_license'
    ]
    
    for table in required_tables:
        cursor.execute(f\"\"\"
            SELECT EXISTS (
                SELECT FROM information_schema.tables 
                WHERE table_name = '{table}'
            );
        \"\"\")
        if cursor.fetchone()[0]:
            tables_exist.append(table)
    
    print(f'‚úÖ Existing tables: {tables_exist}')
    
    # Check field structure in configurations table if it exists
    if 'configurations_tradingconfiguration' in tables_exist:
        cursor.execute(\"\"\"
            SELECT column_name 
            FROM information_schema.columns 
            WHERE table_name = 'configurations_tradingconfiguration'
            AND column_name LIKE '%fib%'
            ORDER BY column_name;
        \"\"\")
        
        fib_fields = [row[0] for row in cursor.fetchall()]
        print(f'üìã Current Fibonacci fields: {fib_fields}')
        
        # Determine if we have old or new field names
        old_field_pattern = any('fib_level_' in field for field in fib_fields)
        new_field_pattern = any('fib_session_' in field or 'fib_primary_' in field for field in fib_fields)
        
        if old_field_pattern and not new_field_pattern:
            print('üîÑ OLD field structure detected - field rename migration needed')
            field_structure['migration_needed'] = True
            field_structure['current_structure'] = 'old'
        elif new_field_pattern and not old_field_pattern:
            print('‚úÖ NEW field structure detected - migration already completed')
            field_structure['migration_needed'] = False
            field_structure['current_structure'] = 'new'
        elif old_field_pattern and new_field_pattern:
            print('‚ö†Ô∏è  MIXED field structure detected - partial migration state')
            field_structure['migration_needed'] = True
            field_structure['current_structure'] = 'mixed'
        else:
            print('‚ùì Unknown field structure - will create fresh migrations')
            field_structure['migration_needed'] = True
            field_structure['current_structure'] = 'unknown'
    else:
        print('üìù No configurations table - fresh installation')
        field_structure['migration_needed'] = True
        field_structure['current_structure'] = 'fresh'

# Store the analysis result
import json
with open('/tmp/db_analysis.json', 'w') as f:
    json.dump({
        'tables_exist': tables_exist,
        'field_structure': field_structure
    }, f)

print(f'üíæ Database analysis complete - {len(tables_exist)} tables found')
"

# Load analysis results
DB_ANALYSIS=$(python -c "
import json
try:
    with open('/tmp/db_analysis.json', 'r') as f:
        data = json.load(f)
    print(f\"{data['field_structure']['migration_needed']}|{data['field_structure']['current_structure']}|{len(data['tables_exist'])}\")
except:
    print('True|unknown|0')
")

IFS='|' read -r MIGRATION_NEEDED CURRENT_STRUCTURE TABLE_COUNT <<< "$DB_ANALYSIS"

echo "üìä Migration Strategy: $CURRENT_STRUCTURE structure, $TABLE_COUNT tables, migration needed: $MIGRATION_NEEDED"

# Handle migrations based on current state
if [ "$CURRENT_STRUCTURE" = "fresh" ] || [ "$TABLE_COUNT" = "0" ]; then
    echo "üÜï Fresh installation - creating initial migrations..."
    
    # Clean any existing migration files for fresh start
    for app in licenses configurations; do
        if [ -d "$app/migrations" ]; then
            find "$app/migrations/" -name "*.py" -not -name "__init__.py" -delete 2>/dev/null || true
            find "$app/migrations/" -name "__pycache__" -type d -exec rm -rf {} + 2>/dev/null || true
            echo "   Cleaned $app migrations"
        fi
    done
    
    # Create fresh migrations with new field names
    python manage.py makemigrations configurations --noinput || {
        echo "‚ùå Failed to create configurations migrations"
        exit 1
    }
    
    python manage.py makemigrations licenses --noinput || {
        echo "‚ùå Failed to create licenses migrations"
        exit 1
    }
    
    # Apply migrations
    python manage.py migrate --noinput || {
        echo "‚ùå Failed to apply fresh migrations"
        exit 1
    }
    
    echo "‚úÖ Fresh migrations applied successfully"

elif [ "$CURRENT_STRUCTURE" = "old" ]; then
    echo "üîÑ Old field structure detected - applying field rename migrations..."
    
    # Create field rename migration
    echo "üìù Creating field rename migration..."
    python manage.py makemigrations configurations --name rename_fib_fields --noinput || {
        echo "‚ùå Failed to create field rename migration"
        exit 1
    }
    
    # Apply the field rename migration
    echo "üöÄ Applying field rename migration..."
    python manage.py migrate configurations --noinput || {
        echo "‚ùå Failed to apply field rename migration"
        exit 1
    }
    
    # Apply any other pending migrations
    python manage.py migrate --noinput || {
        echo "‚ùå Failed to apply remaining migrations"
        exit 1
    }
    
    echo "‚úÖ Field rename migration completed successfully"

elif [ "$CURRENT_STRUCTURE" = "new" ]; then
    echo "‚úÖ New field structure already in place - checking for pending migrations..."
    
    # Just apply any pending migrations
    python manage.py migrate --noinput || {
        echo "‚ùå Failed to apply pending migrations"
        exit 1
    }
    
    echo "‚úÖ All migrations up to date"

else
    echo "‚ö†Ô∏è  Mixed or unknown field structure - attempting recovery..."
    
    # Try to create and apply migrations carefully
    python manage.py makemigrations --noinput || echo "‚ö†Ô∏è  Migration creation had issues"
    python manage.py migrate --noinput || {
        echo "‚ùå Migration failed - manual intervention may be required"
        exit 1
    }
    
    echo "‚úÖ Recovery migration completed"
fi

# Verify final database state
echo "üîç Final database verification..."
python -c "
import os, django
os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'trading_admin.settings_render')
django.setup()

from django.db import connection

required_tables = [
    'configurations_tradingconfiguration',
    'licenses_client',
    'licenses_license'
]

missing_tables = []
with connection.cursor() as cursor:
    for table in required_tables:
        cursor.execute(f\"\"\"
            SELECT EXISTS (
                SELECT FROM information_schema.tables 
                WHERE table_name = '{table}'
            );
        \"\"\")
        if not cursor.fetchone()[0]:
            missing_tables.append(table)

if missing_tables:
    print(f'‚ùå Still missing tables: {missing_tables}')
    exit(1)
else:
    print('‚úÖ All required tables verified')

# Verify new field structure
try:
    cursor.execute(\"\"\"
        SELECT column_name 
        FROM information_schema.columns 
        WHERE table_name = 'configurations_tradingconfiguration'
        AND (column_name LIKE 'fib_session_%' OR column_name LIKE 'fib_primary_%')
        ORDER BY column_name;
    \"\"\")
    
    new_fields = [row[0] for row in cursor.fetchall()]
    if new_fields:
        print(f'‚úÖ New field structure confirmed: {len(new_fields)} new fields found')
        print(f'   Sample fields: {new_fields[:3]}...')
    else:
        print('‚ö†Ô∏è  New field structure not detected - may still have old fields')
        
except Exception as e:
    print(f'‚ö†Ô∏è  Field verification failed: {e}')

print('‚úÖ Database verification completed')
"

# Collect static files
echo "üìÅ Collecting static files..."
python manage.py collectstatic --noinput --clear || echo "‚ö†Ô∏è  Static collection failed"

echo ""
echo "‚úÖ DEPLOYMENT COMPLETED SUCCESSFULLY!"
echo "üåê Application URLs:"
echo "   üì± Admin: https://mt5-dashboard.onrender.com/admin/"
echo "   üè† Dashboard: https://mt5-dashboard.onrender.com/dashboard/"
echo "   ü§ñ API: https://mt5-dashboard.onrender.com/api/validate/"
echo ""
echo "üîÑ Field Rename Migration Status:"
echo "   ‚úÖ Database schema updated with new field names"
echo "   ‚úÖ API maintains backward compatibility"
echo "   ‚úÖ Existing data preserved during migration"
echo ""
echo "üìù Next Steps:"
echo "   1. Test admin interface with new field names"
echo "   2. Verify API endpoints return both old and new field names"
echo "   3. Confirm existing MT5 robots continue working"
echo ""

# Start the application (MUST be the last line)
echo "üéØ Starting application server on port ${PORT:-10000}..."
exec gunicorn trading_admin.wsgi:application \
    --bind "0.0.0.0:${PORT:-10000}" \
    --workers 3 \
    --timeout 120 \
    --max-requests 1000 \
    --max-requests-jitter 100 \
    --preload \
    --log-level info \
    --access-logfile - \
    --error-logfile -